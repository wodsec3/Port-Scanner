# input from user Ip or Host address to scan

ip=input('Enter Host address : ')
# get ip with name
socket.gethostbyname(ip)
# set default time out
socket.setdefaulttimeout(0.2)
# {for} loop for range 1, 65535 
for port in range(1,65535):
    # create tcp connection
    s=socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    # connecting to host with port
    result=s.connect_ex((ip, port))
    # if result == 0 its true, port open
    if result == 0:
        print('[+] Port is OPEN(%s) -- at: %s %s'%(port, datetime.datetime.now(), '-'*20))
    # else result != 0 its false, port is close
    else:
        print('[-] Port is CLOSE(%s) -- at: %s'%(port, datetime.datetime.now()))
